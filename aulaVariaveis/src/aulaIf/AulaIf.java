package aulaIf;

public class AulaIf {

	public static void main(String[] args) {

		int valorInteiro = 9;

		if (valorInteiro > 11) {
			System.out.println("verdade");
		} else if (7 > 2) {
			System.out.println("verdade 2");
		} else if (3 < 4 && 5 > 8) {
			System.out.println("verdade 3");
		} else {
			System.out.println("todas sao falsas");
		}

	}
}

